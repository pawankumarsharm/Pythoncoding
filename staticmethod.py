class DurgaMath:

	@staticmethod
	def add(x,y):
		print('The Sum:',x+y)
		
	@staticmethod
	def product(x,y):
		print('The Product:',x*y)
		
	@staticmethod
	def average(x,y):
		print('The Average Value:',(x+y)/2)
		
DurgaMath.add(10,20)
DurgaMath.product(10,20)
DurgaMath.average(10,20)

'''The Sum: 30
The Product: 200
The Average Value: 15'''